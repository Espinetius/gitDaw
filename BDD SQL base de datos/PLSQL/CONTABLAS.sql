CREATE OR REPLACE PROCEDURE InsertarEmple (
  NUMEMPLE    NUMBER,
  APELLIDOEMPLE  VARCHAR2,
  OFICIOEMPLE    VARCHAR2,
  NUMDIR       NUMBER,
  FECHAALT DATE,
  SUELDO   NUMBER,
  COMISIONES  NUMBER,
  DEPTNO   NUMBER)
IS   
BEGIN
  NUMEMPLE:=&INTRODUZCAELNUMERODEEMPLEADO;
  APELLIDOEMPLE:=&INTRODUZCAELAPELLIDODELEMPLEADO;
  OFICIOEMPLE:=&INTRODUZCAELPUESTO;
  NUMDIR:=&INTRODUZCANUMERODIRECTOR;
  FECHAALT:=&INTRODUZCAFECHADEALTA;
  SUELDO:=&INTRODUZCASALARIO; 
  COMISIONES:=&INTRODUZCACOMISION;
  DEPTNO:=&INTRODUZCANUMERODEPARTAMENTO;
  SELECT * INTO NUMEMPLE, APELLIDOEMPLE, OFICIOEMPLE,
  NUMDIR,FECHAALT,SUELDO,COMISION,DEPTNO FROM EMPLE;
END;